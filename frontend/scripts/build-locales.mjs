function set(obj, keyPath, value) {
  const parts = keyPath.split(".");
  let cur = obj;
  for (let i = 0; i < parts.length - 1; i++) {
    cur[parts[i]] ??= {};
    cur = cur[parts[i]];
  }
  cur[parts[parts.length - 1]] = value;
}

function fromFlat(flat) {
  const obj = {};
  for (const [k, v] of Object.entries(flat)) set(obj, k, v);
  return obj;
}

